    private void btnNDatosAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNDatosAActionPerformed
        // TODO add your handling code here:
        Muestra.ConjuntoCa(Integer.parseInt(txtCa.getText()));
        btnDatosA.setText("Dato: 1/"+Muestra.LongA());
        txtCa.setText(null);
        txtCa.requestFocus();
        btnNDatosA.setEnabled(false);
    }//GEN-LAST:event_btnNDatosAActionPerformed

    private void btnDatosAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDatosAActionPerformed
        // TODO add your handling code here:
        Muestra.Ca(txtCa.getText(),Contador-1);
        txtCa.setText(null);
        txtCa.requestFocus();
        Contador++;

        if (Contador>Muestra.LongA()){
            for(int i=1;i<=Muestra.LongA();i++){
                txtCa.setText(txtCa.getText()+Muestra.Ca(i-1)+";");
            }
            txtCa.setText("{"+txtCa.getText()+"}");
            Contador=1;
            btnDatosA.setEnabled(false);
        }
        else{
            btnDatosA.setText("Dato: "+Contador+" / "+Muestra.LongA());
        }

    }//GEN-LAST:event_btnDatosAActionPerformed

