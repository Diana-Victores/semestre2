    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(Union.isSelected()) {
            txtCr.setText("{" +Muestra.Union()+"}");
            lblRes.setText("R = A"+ " U " +"B");
        }
        if(inter.isSelected()){
            txtCr.setText("{" +Muestra.Interseccion()+"}");
            lblRes.setText("R = A"+ " ∩ "+ "B");
        }
        if(Diferencia.isSelected()){
            txtCr.setText("{" +Muestra.Diferencia()+"}");
            lblRes.setText("R = A − B");
        }
        if(D.isSelected()){
            txtCr.setText("{" +Muestra.DiferenciaSimetrica()+"}");
            lblRes.setText("R = A ∆ B");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

