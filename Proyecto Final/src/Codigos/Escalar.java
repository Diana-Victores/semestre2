    private void CmdCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CmdCalcActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) result.getModel();
        modelo.setRowCount(n);
        modelo.setColumnCount(m);
        result.setModel(modelo);
        double E=0;
        E=Double.parseDouble(TxtE.getText());
        double[][] matrixA = new double[n][m]; double[][] matrixR = new double[n][m];
        for(int i=0; i<n; i++){
            for(int j=0; j<m; j++){
                matrixA[i][j]=Double.parseDouble(Matriz1.getValueAt(i, j).toString());
                matrixR[i][j]=E*matrixA[i][j];
                result.setValueAt(matrixR[i][j], i, j);
            }
        }
    }//GEN-LAST:event_CmdCalcActionPerformed

