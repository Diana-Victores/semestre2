    private void CmdDefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CmdDefActionPerformed

        n=Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese Filas"));
        m=Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese Columnas"));
        double[][] matrixA = new double[n][m];
        for(int i=0; i<n; i++){
            for(int j=0; j<m; j++){
                matrixA[i][j]=Double.parseDouble(JOptionPane.showInputDialog(null, "Ingrese Elemento "+i+","+j));
            }
        }
        DefaultTableModel modeloA = (DefaultTableModel) Matriz1.getModel();
        modeloA.setRowCount(n);
        modeloA.setColumnCount(m);
        Matriz1.setModel(modeloA);
        for(int i=0; i<n; i++){
            for(int j=0; j<m; j++){
                Matriz1.setValueAt(String.valueOf(matrixA[i][j]), i, j);
            }
        }
    }//GEN-LAST:event_CmdDefActionPerformed

